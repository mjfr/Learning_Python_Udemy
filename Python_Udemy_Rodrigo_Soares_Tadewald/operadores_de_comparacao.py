# -*- coding: utf-8 -*-

a = 1
b = 2
c = 1

#Usado para comparar dados e obter um boolean como resposta

#É igual?
print(a == b)

#É diferente?
print(a != b)

#É maior?
print(a > b)

#É menor?
print(a < b)

#É menor ou igual?
print(a <= b)

#É maior ou igual?
print(a >= b)

print('\n\n\n')

#Operadores de comparação em cadeia (and or)

#Ambos os casos são iguais?
print(a == b and a == c)

#Algum caso é verdadeiro?
print(a == b or a == c)

#Ambos os casos são diferentes?
print(a != b and a != c)

#Ambos os casos são menores ou iguais?
print(a <= b and a <= c)

#Ambos os casos são maiores ou iguais?
print(a >= b and a >= c)

#Algum caso é maior ou igual?
print(a >= b or a >= c)
